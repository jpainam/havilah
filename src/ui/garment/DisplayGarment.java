    private  void tableValueChanged(ListSelectionEvent e){
        if(table.getSelectedRow()  >= 0){
            jbDeleteGarment.setEnabled(true);
            jbEditGarment.setEnabled(true);
            jbDetailsGarment.setEnabled(true);
        }else{
            jbDeleteGarment.setEnabled(false);
            jbEditGarment.setEnabled(false);
            jbDetailsGarment.setEnabled(false);
        }
    }
