    private void jbSearchGarmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSearchGarmentActionPerformed
        ArrayList<Garment> employee;
        employee = (ArrayList<Garment>) DBGarment.searchGarment(jtSearchGarment.getText());
        table.removeAll();
        table.setModel(new GarmentJTableModel(employee));
        /** Reapply the JTableModel */
        table.getColumnModel().getColumn(0).setHeaderValue("NÂ°");
        table.getColumnModel().getColumn(0).setPreferredWidth(0x2);
        table.getColumnModel().getColumn(0).setWidth(0x2);
        Dimension d = table.getPreferredScrollableViewportSize();
        d.width = getPreferredSize().width;
        table.setPreferredScrollableViewportSize(d);
        table.setRowHeight(table.getRowHeight());
        RowHeadersRenderer rowRenderer = new RowHeadersRenderer();
        //gridStudent.setDefaultRenderer(String.class, rowRenderer); // This does not work
        table.getColumnModel().getColumn(0).setCellRenderer(rowRenderer);
        table.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }//GEN-LAST:event_jbSearchGarmentActionPerformed
    private String getId(){
        int row = table.getSelectedRow();
        return table.getValueAt(row, 1).toString();
    }
    /**
     * Launch the Edit Panel used to edit the Garment
     * @param evt 
     */
    private void jbEditGarmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEditGarmentActionPerformed
        String ssn = getId();
        GUI g = GUI.getInstance();
        g.getContentPane().removeAll();
        g.setContentPane(new EditGarment(DBGarment.getData(ssn)));
        g.repaint();
        g.pack();

    }//GEN-LAST:event_jbEditGarmentActionPerformed

    private void jbDeleteGarmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbDeleteGarmentActionPerformed
        if(DBGarment.deleteData(getId())){
            JOptionPane.showMessageDialog(null, "Garment deleted with success");
            /* Retirer une ligne du tableau quand supprimer est ok */
            //table.updateUI();
            GUI g = GUI.getInstance();
            g.getContentPane().removeAll();
            g.setContentPane(new DisplayEmployee());
            g.repaint();
            g.pack();
        }else{
            JOptionPane.showMessageDialog(null, "Error in deleting the garment");
        }
    }//GEN-LAST:event_jbDeleteGarmentActionPerformed

